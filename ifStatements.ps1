# if else examples

$i = 5

# if condition

if ($i -eq 5)
{

    #print output and making text colour green
    Write-Host "condition is true $i is equal to 5 `r" -ForegroundColor Green

}

if ($i -lt 6)
{

    #print output and making text colour green
    Write-Host "condition is true $i is less than 5`r" -ForegroundColor Blue

}

if ($i -gt 4)
{

    #print output and making text colour green
    Write-Host "condition is true $i is greater than 4 `r" -ForegroundColor Yellow

}

if ($i -ge 2)
{

    #print output and making text colour green
    Write-Host "condition is true $i is greater than or equal to 2 `r" -ForegroundColor Magenta

}

if ($i -ge 5)
{

    #print output and making text colour green
    Write-Host "condition is true $i is greater than or equal to 5 `r" -ForegroundColor Green

}

if ($i -le 7)
{

    #print output and making text colour green
    Write-Host "condition is true $i is less than or equal to 7`r" -ForegroundColor Blue

}

if ($i -le 5)
{

    #print output and making text colour green
    Write-Host "condition is true $i is less than or equal to 5`r" -ForegroundColor Yellow

}

if ($i -ne 2)
{

    #print output and making text colour green
    Write-Host "condition is true $i is not equal to 2" -ForegroundColor Magenta

}